      	<h3 class="title" style="font-size: 1.6em; color: #32af23;">Charapita</h3>
      	<p class="banner_text_w3layouts font-all" style="padding: 0; color: rgb(186, 231, 255);"> 
The pod type `Charapita' or Aji Charapa' is found in the Peruvian rainforest, close to the city of Iquitos, where the people are called charapas. The pod is very small (measuring only 0.6 cm in diameter), spherical, thin-fleshed, held upright, and very hot, and matures to a red or yellow color. A similar small pod type from Brazil is called 'pimento de cherio', the word cherio in Portuguese meaning odorous (a feature of all C. chinense fruits is their strong aroma, which has been described as apricot-like by some, while others have claimed the smell is like lanolin).
</p>
      	<p class="banner_text_w3layouts font-all" style="padding: 0; color: rgb(186, 231, 255);"> 
‘Charapita’ is a small-fruited pod type from Brazil, also called pimenta do cheiro. The word cheiro in Portuguese translates as “odorous.” A feature of Charapita fruits is the strong aromatic scent from the pods. ‘Charapita’ is a wild variety found in the Peruvian jungle close to the city of Iquitos, where the people are called charapas; thus the name of the chile pepper. The spherical pod is very small (less than ¼ inch in diameter) and very hot. The thin-fleshed pods, which resemble chiltepíns, start as a very light yellow or ivory and mature into rich yellow. Unlike the chiltepíns, these pods stay attached to the plant when ripe. They are eaten fresh with foods or used as an ingredient in preparing all types of dishes.
